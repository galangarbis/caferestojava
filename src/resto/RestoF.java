    private void writeData(PrintWriter out) throws IOException {
        out.print("transaksi ke : " + (k + 1));
        out.println("");
        out.println("Pesanan     Jumlah     Harga     Total");
        for (int e = 0; e < i; e++) {
            out.println("");
            out.print(Tabel.getValueAt(e, 0) + "     ");
            out.print(Tabel.getValueAt(e, 1) + "     ");
            out.print(Tabel.getValueAt(e, 2) + "     ");
            out.print(Tabel.getValueAt(e, 3));
        }
        out.println("");
        out.println("");
        out.println("harga total = Rp" + TEXTharga.getText());
        out.println("pembayaran  = Rp" + TEXTbayar.getText());
        out.println("kembalian   = Rp" + TEXTkembalian.getText());
        out.println("");
        out.println("");
        out.println("");
        out.println("");
        k++;
    }

