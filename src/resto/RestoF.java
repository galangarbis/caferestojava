    private void Bayar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bayar
// TODO add your handling code here:
        try {
            int a = Integer.parseInt(TEXTbayar.getText());
            int b = Integer.parseInt(TEXTharga.getText());
            if ((Tabel.getValueAt(0, 0) != null) && (a >= b)) {
                int c = a - b;
                String baru4 = Integer.toString(c);
                TEXTkembalian.setText(baru4);
                Calendar d = new GregorianCalendar();
                String s = String.format("%1$te%1$tb%1$ty", d);
                try {
                    PrintWriter out = new PrintWriter(new FileWriter("d:\\transaksi" + s.toString() + ".txt", true));
                    writeData(out);
                    out.close();
                } catch (IOException ex) {
                    ex.printStackTrace();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Salah Input", "RestoKu", JOptionPane.ERROR_MESSAGE);

        }
        int a = Integer.parseInt(TEXTbayar.getText());
        int b = Integer.parseInt(TEXTharga.getText());
        if ((Tabel.getValueAt(0, 0) != null) && (a < b)) {
            TEXTkembalian.setText("UANG TIDAK CUKUP");
            JOptionPane.showMessageDialog(null, "Uang tidak cukup", "RestoKu", JOptionPane.ERROR_MESSAGE);
        }

        if ((Tabel.getValueAt(0, 0) != null) && (a >= b)) {
            int id = Integer.parseInt(TEXTid.getText());
            int idcount = id + 1;
            String idcount2 = Integer.toString(idcount);

            TEXTid.setText(idcount2);
        }
        if ((Tabel.getValueAt(0, 0) != null) && (a < b)) {
            int id = Integer.parseInt(TEXTid.getText());
            int idcount = id;
            String idcount2 = Integer.toString(idcount);

            TEXTid.setText(idcount2);
        }
        if (TEXTbayar.getText() == " ") {
            TEXTkembalian.setText("salah");
        }
    }//GEN-LAST:event_Bayar

